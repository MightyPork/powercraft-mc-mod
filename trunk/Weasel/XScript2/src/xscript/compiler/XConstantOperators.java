package xscript.compiler;

import xscript.compiler.XTree.XConstant;

public class XConstantOperators {

	public static XConstant add(XConstant c1, XConstant c2){
		switch(c1.getTag()){
		case FLOATLITERAL:
			
		case DOUBLELITERAL:
			
		case LONGLITERAL:
			
		case INTLITERAL:
			
		case CHARLITERAL:
			
		case STRINGLITERAL:
			
		default:
			break;
		}
		return null;
	}
	
}
